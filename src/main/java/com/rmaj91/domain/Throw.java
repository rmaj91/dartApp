package com.rmaj91.domain;

public class Throw {
    private int value;
    private int mulitplier;

    public Throw(int value, int mulitplier) {
        this.value = value;
        this.mulitplier = mulitplier;
    }

    public int getValue() {
        return value;
    }

    public int getMulitplier() {
        return mulitplier;
    }
}
